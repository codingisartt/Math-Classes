public class Main {//Math classes are static methods so we dont neet to generate an object.
    public static void main(String[] args) {
        System.out.println(Math.abs(-100)); //absolute value
        System.out.println(Math.cbrt(8));
        System.out.println(Math.ceil(2.4));
        System.out.println(Math.floor(1.99));
        System.out.println(Math.log10(100));
        System.out.println(Math.max(100,200));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.random()*100);
        System.out.println(Math.round(10.49));//int
        System.out.println(Math.rint(10.44));//double
        System.out.println(Math.signum(-59)); // sign
        System.out.println(Math.sqrt(81));
        
    }
}